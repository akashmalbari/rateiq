import { getSessionFromRequest, hasTradingAccess } from '../../../lib/trading/auth';
import { getSiteContent, upsertSiteContent } from '../../../lib/trading/db';
import { compareRankedSignals, rankTradingSignal } from '../../../lib/trading/ranking';
import { runDailyScanner } from '../../../lib/trading/scanner';
import { getScannerUniverse } from '../../../lib/trading/universe';

function requireTradingUser(req, res) {
  const session = getSessionFromRequest(req);
  if (!hasTradingAccess(session)) {
    res.status(401).json({ error: 'Unauthorized' });
    return false;
  }
  return true;
}

function todayKey() {
  return new Date().toISOString().slice(0, 10);
}

function wantsRefresh(req) {
  return req.query?.refresh === '1' || req.query?.refresh === 'true';
}

function parsePositiveInt(value, fallback, { min = 1, max = 100 } = {}) {
  const parsed = Number(value);
  if (!Number.isFinite(parsed) || parsed <= 0) return fallback;
  return Math.max(min, Math.min(max, Math.floor(parsed)));
}

function topSignalCount() {
  return parsePositiveInt(process.env.TRADING_TOP_SIGNAL_COUNT || process.env.DAILY_SIGNALS_EMAIL_COUNT || '10', 10, {
    min: 1,
    max: 10,
  });
}

function liveRefreshEnabled() {
  return process.env.TRADING_ENABLE_HEAVY_REFRESH === '1';
}

function activeUniverse() {
  return getScannerUniverse(process.env.TRADING_SCANNER_UNIVERSE || 'curated_100');
}

function canBuildLiveScan() {
  return liveRefreshEnabled() || activeUniverse().length <= 100;
}

function rankStoredSignals(results = []) {
  return results
    .map((row) => ({
      ...row,
      rankingScore: Number.isFinite(Number(row?.rankingScore)) ? Number(row.rankingScore) : rankTradingSignal(row),
    }))
    .sort(compareRankedSignals)
    .slice(0, topSignalCount());
}

async function buildFreshScan(key) {
  const scan = await runDailyScanner({ strategy: 'momentum' });

  try {
    await upsertSiteContent(key, {
      source: 'server_refresh',
      ...scan,
    });
  } catch (error) {
    console.warn('[api/trading/scan-results] unable to persist live scan', error);
  }

  return scan;
}

export default async function handler(req, res) {
  if (!requireTradingUser(req, res)) return;

  if (req.method === 'GET') {
    const key = `daily_signals_scan_${todayKey()}`;
    const forceRefresh = wantsRefresh(req);

    try {
      const current = await getSiteContent(key);
      if (current?.content && Array.isArray(current.content.topSignals) && !forceRefresh) {
        return res.status(200).json({
          ok: true,
          key,
          source: 'stored',
          ...current.content,
        });
      }

      if (!canBuildLiveScan()) {
        if (current?.content && Array.isArray(current.content.topSignals)) {
          return res.status(200).json({
            ok: true,
            key,
            source: 'stored_large_universe',
            message:
              'Large-universe scans are generated by the scheduled daily job to avoid server timeouts. Showing the latest stored top-ranked results.',
            ...current.content,
          });
        }

        return res.status(200).json({
          ok: true,
          key,
          source: 'pending',
          generatedAt: null,
          scanned: activeUniverse().length,
          signalCount: 0,
          topSignalCount: 0,
          topSignals: [],
          failures: [],
          message:
            'No stored daily scan is available yet. Large-universe scans are generated by cron; manual rebuild is disabled unless TRADING_ENABLE_HEAVY_REFRESH=1.',
        });
      }
    } catch (error) {
      console.warn('[api/trading/scan-results] stored read failed, falling back to live scan', error);
    }

    try {
      const scan = await buildFreshScan(key);
      return res.status(200).json({
        ok: true,
        key,
        source: 'live',
        ...scan,
      });
    } catch (error) {
      return res.status(502).json({ ok: false, error: error.message || 'Failed to build live scan results' });
    }
  }

  if (req.method !== 'POST') return res.status(405).json({ error: 'Method not allowed' });

  try {
    const { results = [], scanned = 0, withCandles = 0, quoteOnly = 0, failed = 0 } = req.body || {};
    if (!Array.isArray(results)) return res.status(400).json({ error: 'results must be an array' });

    const rankedResults = rankStoredSignals(results);
    const key = `daily_signals_scan_${todayKey()}`;
    await upsertSiteContent(key, {
      source: 'apex_ui',
      generatedAt: new Date().toISOString(),
      scanned,
      signalCount: results.length,
      topSignalCount: rankedResults.length,
      withCandles,
      quoteOnly,
      failed,
      topSignals: rankedResults,
    });

    return res.status(200).json({ ok: true, key, stored: rankedResults.length });
  } catch (error) {
    return res.status(500).json({ ok: false, error: error.message || 'Failed to store scan results' });
  }
}
